//計算量はn^3
int main(){
    ll n;
    cin>>n;
    vector<vector<ll>> d;
    //dは隣接する頂点i, jの重みで初期化
    //d[i][i]=0, i, j間に辺が存在しなければd[i][j]=d[j][i]=inf とする

    for (int k = 0; k < n; k++){       // 経由する頂点
        for (int i = 0; i < n; i++) {    // 始点
            for (int j = 0; j < n; j++) {  // 終点
                d[i][j] = min(d[i][j], d[i][k] + d[k][j]);
            }
        }
    }
}